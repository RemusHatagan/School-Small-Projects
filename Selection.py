# Selection
# Also called conditionals
# Allows a user to make a choice
# Allows the code to branch
# Based on a choice
# Has 3 parts : if/elif/else

age = int(input("How old are you? "))

if age < 12:
    print("Too young to watch spider-man")
if age > 18:
    print("You can watch an 18 movie")


